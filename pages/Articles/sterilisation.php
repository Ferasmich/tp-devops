                <h2>Pourquoi la stérilisation est-elle un acte responsable ?</h2>
<p>
<span class='m-4'></span>Par manque d’information ou victime d’idées reçues, vous vous interrogez encore sur l’intérêt de stériliser votre chat.<br/>
La stérilisation est l’une des solutions pour prévenir certains problèmes de santé, réduire des troubles comportementaux du chat et de la chatte et multiplier par deux leur espérance de vie. 
Elle est un enjeu global de protection animale et gage de nombreux bénéfices tant pour la sérénité du propriétaire que pour la santé et le bien-être de son compagnon.<br/>
</p>
<p>
<span class='m-4'></span>Les chats stérilisés vivent 2 fois plus longtemps et se sociabilisent plus facilement. En évitant les fugues et les bagarres, ils limitent les risques d’infection et la transmission de maladies graves.<br/>
</p>
<p>
<span class='m-4'></span>L’espérance de vie du chat entier est plus courte que celle du chat castré, parce que lors des bagarres, l’animal peut contracter le virus de la leucose féline (FeLV) ou du sida du chat (FIV) à l’occasion de morsures ou de griffures.<br/>
</p>
<p>
<span class='m-4'></span>Les propriétaires (et leurs voisins !) n’ont plus à subir les désagréments du marquage urinaire des mâles, ou des vocalises des femelles. L’espèce féline est très prolifique : 
en moins de 3 ans un couple de chats peut être l’origine de plus de 300 descendants. La stérilisation évite ainsi les naissances non désirées, la régulation des naissances permet d’empêcher la prolifération des chats errants et les abandons.<br/>
</p>
<p>
<span class='m-4'></span>En France, plus de la moitié de la population féline est stérilisée (le taux dépasse 80% dans d’autres pays), mais ce n’est pas suffisant...<br/>
Les effets bénéfiques de la stérilisation sur votre animal et sur votre sérénité.
</p>
<h3 class='m-3'>Pour le mâle :</h3>
<p><span class='m-4'></span>Plus de nuisances (marquages urinaires, miaulements nocturnes)
Diminution des risques de fugues, d’accidents, de blessures Diminution des naissances non contrôlées Protection contre les maladies infectieuses virales (Leucose Féline et Sida du Chat)
Comportement plus apaisé (moins de bagarres)
Durée de vie doublée
</p>
<h3 class='m-4'>Pour la femelle :</h3>
<p>
<span class='m-4'></span>Plus de chaleurs (dès l’âge de 6 mois) et donc, plus de miaulements nocturnes, de bagarres et de marquage urinaires des mâles alentours<br/>
Diminution des risques d’accidents, de blessures et de vols<br/>
Diminution des naissances non contrôlées et des abandons<br/>
Protection contre les maladies infectieuses virales (Leucose Féline et Sida du Chat) et hormonales (cancers, tumeurs mammaires)
<br/>
La durée de vie doublée.
</p>

<h2>Comment stériliser son animal ?</h2>
<p>
<span class='m-4'></span>La stérilisation est une intervention chirurgicale de convenance réalisée très couramment, et pour laquelle les risques sont très réduits. Après un examen général attentif (contrôle de la respiration, fréquence cardiaque, hydratation, poids, etc.)...une anesthésie générale est réalisée.<br/>
</p>
<p>
<span class='m-4'></span>L’intervention chirurgicale, qui consiste à retirer les deux ovaires et éventuellement, l’utérus, dure en moyenne 15 minutes et n’occasionne le plus souvent qu’une ou deux petites incisions. S’en suit une phase de réveil rapide. L’animal retourne très vite à ses occupations. Les chats ou chattes sont généralement déposés le matin à jeun en clinique et peuvent être récupérés en fin de journée et reprendre une alimentation normale le soir.<br/>
</p>
<p>
<span class='m-4'></span>Pendant les 2 mois qui suivent l’opération, il est important de surveiller l’alimentation de l’animal en réduisant l’apport calorique : il est souhaitable de choisir un aliment à teneur modérée en matières grasses et d’adapter la ration journalière aux besoins réels du chat.<br/>
</p>

<h3 class='m-4'>La stérilisation de la chatte</h3>
<p>
<span class='m-4'></span>La stérilisation est conseillée à l’âge de 6 mois : l’opération est effectivement plus simple lorsque la chatte N'A PAS encore été en chaleur (ses vaisseaux sont plus petits), tout en ayant déjà son métabolisme adulte.
</p>
<h3 class='m-4'>La stérilisation du chat</h3>
<p>
<span class='m-4'></span>Le chat est pubère (c’est-à-dire que ses testicules deviennent fonctionnels, et qu’il peut se reproduire) vers l’âge de 6/7 mois. Les testicules produisent une hormone, la testostérone, responsable en grande partie du comportement du mâle entier.<br/>
</p>
<p>
<span class='m-4'></span>« Ici, c’est chez moi ! »: le chat, animal territorial, marque son domaine réservé de plusieurs façons : par la projection d’urine, et par des griffades (sur les murs, meubles de la maison, arbres du jardin). C’est le marquage urinaire qui est gênant, parce que l’urine du mâle entier a une odeur très forte et difficile à faire disparaître. Le chat va émettre des petits jets d’urine à environ 10/15 cm de hauteur aux endroits stratégiques (portes, lieux de passage, dans la maison ou le jardin) pour signifier qu’il est bien le maître des lieux.<br/>
</p>
<p>
<span class='m-4'></span>« Je vis ma vie ! » : un mâle non castré sera aussi plus indépendant : il rentrera chez vous pour manger et dormir, et repartira vite chasser, rencontrer des femelles en chaleur ou se bagarrer avec d’autres mâles pour un territoire ou une chatte. Résultat : des plaies, des bosses, des abcès, parfois des accidents (le chat peut parcourir un grand territoire en période de reproduction). Bref,! Sans compter les vocalises bruyantes pendant les amours...<br/>
</p>
<p>
<span class='m-4'></span>Il y a donc tout intérêt à faire stériliser votre chat avant sa puberté, afin qu’il ne prenne pas de mauvaises habitudes difficiles à perdre.<br/>
La stérilisation du mâle implique l’ablation chirurgicale des testicules. La vasectomie (ligature des canaux déférents) ne présente pas d’intérêt car elle rend le chat infertile mais il conserve le comportement d’un chat entier.<br/>
</p>
<h2>Les nombreuses idées reçues de propriétaires autour de la stérilisation</h2>

<span class='m-4'></span>La stérilisation NE CHANGE PAS le caractère de mon animal : La stérilisation ne modifie en aucun cas le caractère et la personnalité de l’animal mais permet de réduire les traits de caractères liés à sa libido et à l’imprégnation de ses hormones : miaulements, marquage urinaire, fugues, bagarres<br/>
<p>La stérilisation ne rend pas obèse : la prise de poids n’est pas un effet secondaire de la stérilisation, mais peut survenir si le propriétaire ne veille pas à donner à son chat un régime t adapté à son mode de vie. La stérilisation entraîne une modification du métabolisme du chat. Une fois stérilisé, il devient moins actif et brûle moins de calories. Si ses besoins en énergie diminuent, son appétit peut avoir tendance à augmenter. Seule une alimentation spécifique, adaptée et proportionnelle à son activité physique lui permettra de garder la ligne.
</p>
<p>
<span class='m-4'></span>La stérilisation peut se pratiquer uniquement après une première portée : Une chatte peut être stérilisée AVANT ses premières chaleurs sans aucune contre-indication. Si la stérilisation peut se faire après une ou plusieurs portées, il faut garder à l’esprit que les gestations fatiguent la chatte et peuvent, dans le temps, nuire à sa santé. De plus, la stérilisation permet de réduire les risques de tumeurs mammaires, à l’inverse de la « pilule » qui peut favoriser certaines maladies (diabète, infection utérine, tumeurs mammaires)<br/><br/>
